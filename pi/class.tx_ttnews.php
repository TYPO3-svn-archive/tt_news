		$this->config = $this->conf;
		$this->arcExclusive = $this->conf['archive'];
		$this->LOCAL_LANG_loaded = FALSE;
		$this->pi_loadLL(); // Loading language-labels


		if ($this->conf['code']) {
			$this->theCode = strtoupper($this->conf['code']);
		}

		if (is_array($this->categories[$row['uid']])) {
